# Performs Operation and Assigns Value to the Variables
# Types of Assignment Operators

# Simple Assignment
x = 5
print('Simple Assignment:', x)

# Add and Assign
x += 3  # x = x + 3
print('Add and Assign:', x)

# Subtract and Assign
x -= 2  # x = x - 2
print('Subtract and Assign:', x)

# Multiply and Assign
x *= 4  # x = x * 4
print('Multiply and Assign:', x)

# Divide and Assign
x /= 2  # x = x / 2
print('Divide and Assign:', x)

# Floor Divide and Assign
x //= 3  # x = x // 3
print('Floor Divide and Assign:', x)

# Modulus and Assign
x %= 5  # x = x % 5
print('Modulus and Assign:', x)

# Exponent and Assign
x **= 2  # x = x ** 2
print('Exponent and Assign:', x)

# Bitwise AND and Assign
x &= 1  # x = x & 1
print('Bitwise AND and Assign:', x)

# Bitwise OR and Assign
x |= 1  # x = x | 1
print('Bitwise OR and Assign:', x)

# Bitwise XOR and Assign
x ^= 1  # x = x ^ 1
print('Bitwise XOR and Assign:', x)

# Bitwise Left Shift and Assign
x <<= 1  # x = x << 1
print('Bitwise Left Shift and Assign:', x)

# Bitwise Right Shift and Assign
x >>= 1  # x = x >> 1
print('Bitwise Right Shift and Assign:', x)
